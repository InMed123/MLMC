                        <p><strong>8/7/14</strong></p>

<p><strong>MOUNTAIN LAKES MEDICAL CENTER PLACES ON
QUALITY HONOR ROLL</strong></p>

<p><strong>ATLANTA –</strong> Mountain Lakes Medical Center in Clayton has been named to the Georgia Hospital Association’s (GHA) Partnership for Health and Accountability (PHA) Core Measures Honor Roll. Mountain Lakes Medical Center is one of 30 hospitals in Georgia to be placed in the Chairman’s category, the highest on the list. The honor roll is based on clinical data provided by the federal Centers for Medicaid & Medicare Services (CMS), which administers the nation’s Medicare and Medicaid programs. The data was collected from January 2013 to December 2013.</p>

<p>Hospitals are required to submit care data to CMS, which details how well a hospital’s caregivers adhere to a list of Value-Based Purchasing (VBP) core measures. These measures are the clinical processes of care that are known to be the most effective methods of treatment for surgical patients and patients who have suffered heart attacks, pneumonia and heart failure.</p>

<p>For instance, a recommended treatment to help prevent a heart attack is to take aspirin either before or upon arrival at the hospital, as well as at discharge. It is recommended that surgery patients are given an antibiotic one hour prior to surgery to prevent infection. The VBP core measure is a composite measure that determines whether or not a patient received the right care at the right time. A hospital’s adherence to these recommended clinical practices usually leads to better outcomes.</p> 

<p>“We are pleased to recognize Mountain Lakes Medical Center’s commitment to patient safety,” said Earl V. Rogers, president of GHA. “Compliance with these important core measures ensures that every patient receives the best, most effective health care possible.”</p>

<p>“We constantly strive to maintain the highest standard of patient care,” said Joseph Forese, chief executive officer of Mountain Lakes Medical Center. “This honor validates that and is a tribute to the dedication of the staff members who ensure that great patient care is made even better.”</p>

<p>Dr. Forese is available for comments.  He may be reached by calling 706-782-3100.</p> 

<p><strong>About MLMC</strong></p>

<p>Mountain Lakes Medical Center is a full service, 25-bed, acute care hospital located in Clayton, Georgia. The hospital boasts a trained and professional medical staff, experienced employees and energetic volunteers who are dedicated to improving healthcare and changing the way people think about their health and wellness.</p>

<p>Mountain Lakes Medical Center provides quality, comprehensive medical care and preventive health services for the Rabun County community and communities throughout Northeast Georgia. With close to 200 associates, the hospital truly affects the physical and economic health of the region.</p>

<p><strong>About PHA</strong></p>

<p>The Partnership for Health and Accountability, formed in January 2000 and sponsored by the Georgia Hospital Association, is a unique statewide collaborative that brings together health care providers with community agencies and individuals to achieve healthy communities. PHA includes representation from groups like hospitals, physicians, state health officials, legislators and businesses.
