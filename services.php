            <h2>
               Sleep Testing Services
            </h2>
            <h4>
               Are you ready to take the first step toward getting a better night’s sleep? 
            </h4>
            <div class="media">
               <p>
                  Mountain Lakes Medical Center offers adult sleep testing services to diagnose and treat a spectrum of sleep disorders.  Problems such as snoring, insomnia and daytime sleepiness may mean that you, like 20 million other Americans, have a sleep disorder.     
               </p>
               <p>
                  Sleep disorders are directly linked to health problems such as heart disease, adult onset diabetes, stroke, ADD in children, and depression.  If sleep problems are causing you to have health problems, making you irritable or depressed, or have affected your concentration, see your doctor to determine if sleep testing can provide answers and solutions.
               </p>
               <div class="row text-box-grey col-md-12">
                  <h4>
                     What are some signs that I may need to talk to my doctor?
                  </h4>
                  <ul class="col-md-6">
                     <li>Loud snoring
                     </li>
                     <li>Awakening yourself with your own snoring
                     </li>
                     <li>Waking up not feeling refreshed 
                     </li>
                     <li>Waking up with headaches
                     </li>
                     <li>Waking up with the sensation of choking</li>
                     <li>Waking up sweating</li>
                     <li>Sleeping at inappropriate times</li>
                  </ul>
                  <ul class="col-md-6">
                     <li>Sleep walking
                     </li>
                     <li>Talking nightmares
                     </li>
                     <li>Disturbing dreams
                     </li>
                     <li>Sleep related seizures
                     </li>
                     <li>Repeated movement of legs during sleep</li>
                     <li>Impotence</li>
                     <li>Dissatisfaction with the quality of sleep</li>
                  </ul>
               </div>
            </div>
            <div class="row text-box-grey col-md-12">
               <h4>How would I know if I have a sleep problem?</h4>
               <p>Significant sleep disorders presently affect approximately 25 percent of the general population. Adults should feel refreshed and ready to face the day after 7.5 to 8 hours of uninterrupted sleep. If you often feel unable to perform throughout the day, this is an indicator of a possible night time problem.</p>
               <h4>How are sleep disorders diagnosed?</h4>
               <p>The starting point is a good sleep history obtained by talking with your physician who can order a study for you if appropriate. The most common sleep disorders are diagnosed by having a sleep study done in a controlled environment which is a bedroom-like setting at the hospital. Patients are monitored throughout their normal sleep period, whether that is at night or even during the day for individuals who perhaps routinely work a night shift.</p>
               <h4>What is a sleep study?</h4>
               <p>Sleep studies at Mountain Lakes Medical Center monitor all activities that occur during the night in a homelike environment. A sleep technologist attaches small metal discs called electrodes to the head and skin with an adhesive, plus other simple probes to measure breathing, heart rate and the blood’s oxygen level.  The technical equipment and technologist are in a nearby room monitoring all activities.</p>
               <h4>Will my insurance cover a sleep study?</h4>
               <p>Most insurance companies cover sleep studies and consider them and a valuable diagnostic tool to aid in managing overall health. </p>
               <h4>How can I schedule a sleep study?</h4>
               <p>Your primary care physician can schedule a study on your behalf or you may contact Mountain Lakes Medical Center directly at 706.782.0412 for further information. </p>
