                        <p><strong>Mountain Lakes Medical Center Earns ACR Accreditation</strong></p>

<p>Mountain Lakes Medical Center has been awarded a three-year term of accreditation in mammography as the result of a recent review by the American College of Radiology (ACR). Mammography is a specific type of imaging test that uses a low-dose x-ray system to examine breasts. A mammography exam, called a mammogram, is used to aid in the early detection and diagnosis of breast diseases in women.</p> 

<p>The ACR gold seal of accreditation represents the highest level of image quality and patient safety. It is awarded only to facilities meeting ACR Practice Guidelines and Technical Standards after a peer-review evaluation by board-certified physicians and medical physicists who are experts in the field. Image quality, personnel qualifications, adequacy of facility equipment, quality control procedures, and quality assurance programs are assessed. The findings are reported to the ACR Committee on Accreditation, which subsequently provides the practice with a comprehensive report they can use for continuous practice improvement.</p> 

<p>The ACR is a national professional organization serving more than 36,000 diagnostic/interventional radiologists, radiation oncologists, nuclear medicine physicians, and medical physicists with programs focusing on the practice of medical imaging and radiation oncology and the delivery of comprehensive health care services. 
