                  <h4>Ultrasound</h4>
                  <p>Ultrasound or Diagnostic Sonography utilizes sound waves to produce images of internal organs and vessels. This information is useful for the diagnosis of a range of pathologies. Every exam is read by a board certified Radiologist and/or Cardiologist. The results are faxed directly to your doctor usually within 24-48 hours of your exam.</p>
