                  <p>The best defense against breast cancer is to find it as early as possible, when it is easiest to treat.  Preventative measures such as screening mammograms can assist in early response to breast cancer. Screening mammograms are performed on women who do not have any symptoms. If something is found after a screening mammogram, then a diagnostic mammogram and possibly an ultrasound are performed to determine if a lump or other symptom is a sign of breast cancer. </p>
