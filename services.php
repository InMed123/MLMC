               <div class="row text-box-grey">
                  <h4>About Mountain Lakes Medical Center’s Open MRI</h4>
                  <h4>MRI</H4>
                  <p>Magnetic Resonance Imaging (MRI) is a technology that allows your physician to see inside your body without surgery or potentially harmful ionizing radiation.  MRI utilizes a strong magnetic field along with radio waves to produce an image.  </p>
                  <h4>What is an Open MRI?</h4>
                  <p>Open MRI technology is designed for patients that are claustrophobic or anxious when placed in a confined place and are sensitive to noises.  Unlike closed MRI, Open MRI technology is open on three sides to give you the feeling of unobstructed space in a relaxed atmosphere.</p>
                  <h4>Is there any risk?</h4>
                  <p>Magnetic Resonance Imaging is very safe.  There are no health risks associated with the magnetic or the radio waves used by the machine.  However, some special circumstances limit the use of a magnetic field.  It is important for you to tell us if you have any of the following:</p>
                  <ul>
                     <li>Cardiac pacemaker or artificial heart valve</li>
                     <li>Metal plate, pin or other metallic implant</li>
                     <li>Intrauterine device, such as Copper-7</li>
                     <li>Insulin pump or other infusion pump</li>
                     <li>Aneurysm clips</li>
                     <li>Previous gun wound</li>
                     <li>Inner ear implant</li>
                     <li>Metal fragments in your eye or other parts of your body</li>
                     <li>Permanent (tattoo) eyeliner</li>
                  </ul>
                  <h4>How long will the exam take?</h4>
                  <p>The length of the exam depends on the type of MRI exam your doctor has ordered.  Most exams are completed within 30 to 60 minutes.</p>
                  <h4>How do I get my exam results?</h4>
                  <p>After your exam, a radiologist will review your images and a report will be sent directly to your doctor.  Reports are typically available within 24 hours.  Your doctor will discuss the results of the exam with you.</p>
                  <h4>How can I schedule an appointment?</h4>
                  <p>Mountain Lakes Medical Center Outpatient Imaging Services is conveniently located in Lakemont, Georgia and is open 5 days a week for appointments. 8:30AM to 4:30PM Monday through Thursday. We can be reached at 706-782-0770.</p>
