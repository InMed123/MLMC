                  <h4>CT</h4>
                  <p>CT or Computed Axial Tomography, formerly known as a CAT scan, combines x-ray technology with advanced computer technology.</p>
                  <p>The major benefit of CT is its ability to show internal anatomy in cross-sections, commonly 
                     referred to as slices.
                  </p>
                  <p>The most common analogy used to describe the cross-sectional view is that of a slice out of a loaf of bread. In CT scanning, the slices are very thin millimeter sections capable of revealing tiny abnormalities.</p>
